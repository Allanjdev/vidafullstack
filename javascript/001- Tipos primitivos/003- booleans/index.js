// Booleans
/*Representa verdadeiro ou falso e é usado em lógica condicional. Em JavaScript, um booleano é um tipo de dado que representa um valor lógico: verdadeiro (true) ou falso (false). */

// Atribuindo valores booleanos diretamente a variáveis
let isTrue = true;
let isFalse = false;

// Booleans em expressões
let isGreater = 10 > 5; // isGreater será true
let isEqual = 3 === "3"; // isEqual será false (comparação estrita de tipo e valor)

// Booleans em condições
if (isTrue) {
  console.log("É verdade!");
} else {
  console.log("É falso!");
}

// Booleans em loops
let count = 0;
while (count < 5) {
  console.log(count);
  count++;
}
